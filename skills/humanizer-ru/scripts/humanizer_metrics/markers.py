@dataclass
class MarkerHit:
    category: str
    marker: str
    count: int
    positions: list[int] = field(default_factory=list)


def _find_all(text: str, needle_regex: str) -> list[int]:
    return [m.start() for m in re.finditer(needle_regex, text, re.IGNORECASE)]


def scan_hard_bans(text: str) -> list[MarkerHit]:
    """Находит конструкции из HARD BANS. Любое попадание = провал."""
    hits: list[MarkerHit] = []
    for name, pat in HARD_BANS:
        pos = _find_all(text, pat)
        if pos:
            hits.append(MarkerHit("HARD BAN", name, len(pos), pos))
    return hits


# Частотные пороги из SKILL.md: маркер банится не с первого вхождения, а по
# плотности. «Является» — легитимная связка в меру (термины, определения):
# таблица HARD BANS задаёт «>1 раз на 500 слов». Одиночное вхождение остаётся
# мягким маркером («Кальки»), scan_hard_bans его по-прежнему находит.
FREQ_BANS: dict[str, int] = {"Является": 500}


def effective_hard_bans(hits: list[MarkerHit], words: int) -> list[MarkerHit]:
    """Хард-баны после частотных порогов: то, что реально штрафуется/валит exit.

    Для маркеров из FREQ_BANS попадание остаётся баном только при двух и более
    вхождениях с плотностью выше 1 на N слов. Остальные баны проходят как есть.
    """
    out: list[MarkerHit] = []
    for h in hits:
        per = FREQ_BANS.get(h.marker)
        if per and (h.count < 2 or h.count <= words / per):
            continue
        out.append(h)
    return out


def scan_markers(text: str) -> list[MarkerHit]:
    """Прогоняет 20 категорий быстрого сканера. Возвращает только попадания.

    Элемент с префиксом "re:" трактуется как регулярное выражение,
    остальные — как литеральная подстрока (регистронезависимо).
    """
    hits: list[MarkerHit] = []
    for cat, phrases in SCANNER.items():
        for phrase in phrases:
            if isinstance(phrase, tuple):
                label, pat = phrase
            elif phrase.startswith("re:"):
                label, pat = phrase[3:], phrase[3:]
            else:
                label, pat = phrase, re.escape(phrase)
            pos = _find_all(text, pat)
            if pos:
                hits.append(MarkerHit(cat, label, len(pos), pos))
    return hits


def marker_verdict(marker_hits: list[MarkerHit]) -> str:
    """Шкала из SKILL.md: 0-2 чисто, 3-5 подозрительно, 6+ AI.
    Артефакт копипасты — однозначная улика: вердикт сразу, без шкалы."""
    if any(h.category == "Артефакты копипасты" for h in marker_hits):
        return "артефакты копипасты из чат-бота — текст вставлен из ответа ИИ"
    total = sum(h.count for h in marker_hits)
    if total <= 2:
        return f"{total} — скорее всего чистый текст"
    if total <= 5:
        return f"{total} — подозрительно"
    return f"{total} — AI-генерация с высокой вероятностью"
